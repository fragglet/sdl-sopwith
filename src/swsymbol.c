#define sopsyms_from_data(data, w, h, out)                              \
        { int _i;                                                       \
          for (_i=0; _i<sizeof(out)/sizeof(*(out)); ++_i)               \
             (out)[_i] = sopsym_from_data((data)[_i], (w), (h));        \
        }

